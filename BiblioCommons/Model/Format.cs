namespace BiblioCommons
{
    /// <summary>
    /// A Format instance resource describes the format of a Title
    /// </summary>
    public class Format
    {
        /// <summary>
        /// The ID of the format.
        /// </summary>
        public string id { get; set; }

        /// <summary>
        /// The user-friendly name of the format.
        /// </summary>
        public string name { get; set; }
    }
}
